public class Const {
    public static final float PI_FLOAT = (float) Math.PI;

    /**
     * Radians to degrees
     */
    public static final float RAD2DEG_FLOAT = 180F / PI_FLOAT;

    /**
     * Degrees to radians
     */
    public static final float DEG2RAD_FLOAT = PI_FLOAT / 180F;

    /**
     * Radians to degrees
     */
    public static final double RAD2DEG = (double) RAD2DEG_FLOAT;

    /**
     * Degrees to radians
     */
    public static final double DEG2RAD = (double) DEG2RAD_FLOAT;
}
